package operators;
public class AssignmentOperators {

    public static void main(String[] args) {
        int a = 10;
        System.out.println(a += 10);;
        int b = 23;
        System.out.println(b -= 12);;
        int c = 33;
        System.out.println(c *= 45);;
        int d = 34;
        System.out.println(d /= 56);;
    }
}